    public void mostrarTabla(){
    Map<String,Usuario> listaUsuarios = new HashMap();
    listaUsuarios = cUser.obtenerUsuario();
    
    String[][] tabla = new String[listaUsuarios.size()][7];
    int contadora = 0;
    for(String user: listaUsuarios.keySet()){
        Usuario usuario = listaUsuarios.get(user);
        tabla[contadora][0]= usuario.getCedula();
        tabla[contadora][1]=usuario.getNombreCompleto();
        tabla[contadora][2]=usuario.getApellido();
        tabla[contadora][3]=usuario.getTelefono();
        tabla[contadora][4]=usuario.getEmail();
        tabla[contadora][5]=usuario.getNombreUsuario();
        tabla[contadora][6]=usuario.getPassword();
        contadora++;
    }
    DefaultTableModel model = new DefaultTableModel(tabla,new String[]{"Cedula","Nombre","Apellido","Telefono","Email","Nombre de usuario","Contrase√±a"});
    jTable2.setModel(model);
    }

